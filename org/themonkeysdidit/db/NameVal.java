package org.themonkeysdidit.db;

public class NameVal {
    public NameVal(String name, Object value) {
        NAME = name;
        VALUE = value;
    }
    
    public String getName() {
        return NAME;
    }
    
    public Object getValue() {
        return VALUE;
    }
    
    private String NAME;
    private Object VALUE;
}
